                                Os sentimentos que tomaram conta de mim neste momento foram muitos. Alívio por não ser perseguida novamente; esperançosa de que esse fosse o fim de minhas muitas férias torturantes; e até mesmo orgulhosa por eu mesma ter acabado com isso, tendo me mantido firme ao longo dos anos. O que veio à tona acima de todo o resto, no entanto, foi a curiosidade. É por isso que fiz conforme as instruções e sentei na mesa em frente ao homem misterioso, antecipando as respostas que ele poderia me oferecer.
