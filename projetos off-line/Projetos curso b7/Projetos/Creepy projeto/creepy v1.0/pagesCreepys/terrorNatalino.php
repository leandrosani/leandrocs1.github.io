                                <br/><br/>
                                Eu menti e disse a eles que estava acabado. Desse ponto em diante, ninguém mais teria que saber além de mim. Era minha cruz e somente minha para carregar.
                                <br/><br/>
                                Mais tempo passou e eu cresci. Eu me formei na faculdade e comprei uma casa para mim. A caixa de luz ficou comigo a cada passo do caminho, mas eu nunca recuei, me recusando a deixá-la controlar minha vida. Essa atitude me deu força para enfrentar o trauma e, por um tempo, parecia que estava ganhando. As vésperas de Natal não ficaram mais fáceis, mas o medo e a miséria que experimentei nas consequências que se seguiram estavam desaparecendo mais rápido a cada ano que passava.
                                <br/><br/>
                                Se eu tivesse que conviver com isso, pelo menos eu poderia fazer em meus próprios termos. Era o que eu teria desejado.
