                        Seu pai , em um ato de amor, decidiu construir uma enorme casa de desenho cilíndrico, interligada por rampas e grandes janelas para que sua filha pudesse se movimentar confortavelmente pela casa e desfrutar de toda a vista da cidade.
                        <br/><br/>
                        A construção da casa inovadora começou imediatamente . Dezenas de pedreiros foram reunidos para a tarefa . As pessoas trabalharam até tarde da noite para terminar a construção o mais rápido possível. Meses se passaram e quando a estrutura começou a subir, a tensão e um medo inexplicável começaram a tomar conta deles. De repente, tudo piorou. As ferramentas começaram a desaparecer, os pedreiros culparam uns aos outros sem encontrar nenhuma explicação lógica para o que estava acontecendo .
                        <br/><br/>
                        Os pedreiros chamaram um da obra para encontrar uma solução, o que aconteceu foi que se embriagaram e ninguém apareceu para trabalhar no dia seguinte, exceto três deles .
                        <br/><br/>
