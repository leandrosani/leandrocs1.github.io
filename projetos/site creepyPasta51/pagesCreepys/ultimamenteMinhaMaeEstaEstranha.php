                        &nbsp;&nbsp;&nbsp;&nbsp;Minha mãe sempre foi um pouco peculiar. Ela gostava de fazer as coisas de uma maneira específica e sempre em um dia específico. Havia uma rotina que ela seguia desde que eu era apenas uma criança e não importava a situação, o tempo ou seu humor, a rotina nunca mudaria.
                        <br/><br/>
                        Às segundas-feiras, ela fazia o planejamento. Ela passava meio-dia fazendo uma lista, sempre no papel, de todas as refeições que teríamos naquela semana. Em seguida, ela vasculhava os armários e a geladeira para ver o que ainda tínhamos e acrescentava tudo o que precisávamos à lista, em sua caligrafia perfeita.
                        <br/><br/>
                        Às terças-feiras, ela ia ao mercado de peixes pouco antes do nascer do sol, continuava a visitar o açougue por volta do meio-dia e, finalmente, comprava todos os outros itens de que precisávamos no grande supermercado nos arredores de nosso bairro suburbano.
                        <br/><br/>
                        Às quartas-feiras ela limpava, ou melhor, nós limpávamos. Embora mamãe gostasse das coisas de uma maneira específica e geralmente repassasse tudo o que eu fazia depois de terminar, ela sempre aceitou minha ajuda. Suponho que tenha sido mais para construir meu caráter do que para uma necessidade real de ajuda.
                        <br/><br/>
                        Nas quintas-feiras ela lavava roupa. Quando o sol brilhava ela pendurava as roupas em um varal no jardim e, do contrário, usava a secadora lá dentro. De qualquer forma, as quintas-feiras sempre cheiravam a amaciante de lavanda.
                        <br/><br/>
                        Às sextas-feiras, ela cozinhava. Muffins, bolo e biscoitos. Com muitos temperos e ervas, ela faria suas próprias criações. Esses foram os únicos dias que cheiraram ainda melhor do que as quintas-feiras.
                        <br/><br/>
                        Aos sábados, ela ia beber alguma coisa. Quando eu era mais novo era quando a babá vinha, mas agora ela podia ir sem ter que cuidar disso. Às vezes, ela via seus amigos e voltava com um humor risonho.
                        <br/><br/>
                        Os domingos eram o que chamávamos de dias livres. Foi o único dia que não foi atribuído a uma tarefa específica.
                        <br/><br/>
                        "Trabalhamos durante a semana para que o fim de semana possa ser aproveitado."
                        <br/><br/>
                        Eu ouvi essa frase com mais frequência do que eu poderia contar. Como eu disse, minha mãe sempre foi um pouco peculiar e, à medida que fui crescendo, percebi que ela também era um pouco, senão muito, compulsiva. Em todos os meus anos de vida que morei naquela casinha de subúrbio com minha mãe, nunca a vi se desviar desse horário.
                        <br/><br/>
                        Claro, eu nem sempre estava lá para observar, mas quando voltava da escola às terças-feiras, a geladeira e os armários estariam totalmente abastecidos de novo, as quintas-feiras cheiravam a lavanda e as sextas-feiras sempre tínhamos produtos recém assados. Mesmo nas férias, ela encontrava maneiras de juntar tudo o que precisava ser feito em dias específicos da semana.
                        <br/><br/>
                        Minha mãe nem sempre foi mentalmente estável, o que é algo difícil de testemunhar quando criança e principalmente de admitir. Você quer que seus pais sempre se saiam bem e diga a si mesmo que eles estão bem mesmo quando não estão. No entanto, em muitos aspectos, ela felizmente parecia estar no controle de suas compulsões.
                        <br/><br/>
                        E, acima de tudo, ela sempre foi gentil e amorosa. Para nós dois, apesar das brigas ocasionais. Especialmente quando fiquei mais velha e fiquei ressentida com seus modos um pouco antiquados. Ainda assim, éramos uma família feliz e, em geral, as coisas estavam bem.
                        <br/><br/>
                        Até a semana passada, quando minha mãe, pela primeira vez desde que me lembro, parecia estar fora de sincronia.
                        <br/>
                        -
                        <br/>
                        Tudo começou na segunda-feira quando acordei e encontrei minha mãe na sala assistindo algo estranho na TV. A visão disso já era estranha o suficiente porque normalmente ela nunca ligaria a televisão sozinha antes da noite e agora ela estava sentada lá, usando seu vestido florido e salto alto dentro de casa enquanto assistia algo com foco completo em seus olhos.
                        <br/><br/>
                        "Mãe?" Eu murmurei.
                        <br/><br/>
                        Sua cabeça lentamente virou para a esquerda. Ela olhou para mim e me examinou com precisão. Nenhuma palavra saiu de sua boca. Quando olhei para a tela para ver por que ela estava tão hipnotizada, percebi o ruído branco. Não havia nenhum canal ligado. Sentei-me ao lado dela, tentei encontrar palavras para perguntar o que havia de errado, mas não consegui. Ela estava agindo como se tudo estivesse perfeitamente normal até que papai desceu as escadas com sua pasta na mão.
                        <br/><br/>
                        "Boa tarde, querido. Você está pronto para o almoço?" Ela perguntou a ele. Meu pai inclinou a cabeça, ele percebeu que algo estava errado, mas ele também não mencionou. Em vez disso, ele apenas ergueu um pouco a pasta e disse: "Preciso ir trabalhar".
                        <br/><br/>
                        "Mas é claro", minha mãe sorriu.
                        <br/><br/>
                        Troquei um olhar preocupado com meu pai. Ele definitivamente sabia que algo estava acontecendo e eu me perguntei se eles brigaram na noite passada. Era por isso que minha mãe estava agindo de forma estranha? Antes que eu pudesse falar com ele, no entanto, ele havia desaparecido. E mamãe deu um pulo apenas dois segundos depois.
                        <br/><br/>
                        "Eu preciso ir à lavanderia, você pode fazer algo para comer, sim?"
                        <br/>
                        -
                        <br/>
                        Na terça-feira, não esperava ver minha mãe quando me levantasse. Nesse momento, ela normalmente estaria no mercado de peixes para pegar os peixes mais frescos. Em vez disso, eu a encontrei na mesa da cozinha, nervosamente escrevendo algo. Meu pai estava sentado em frente a ela, comendo ovos mexidos com bacon.
                        <br/><br/>
                        "Você está fazendo uma lista de compras?" Eu perguntei. Era o dia errado, mas pelo menos ela estava agindo um pouco mais normal. Mesmo que ela estivesse usando exatamente o mesmo vestido do dia anterior.
                        <br/><br/>
                        Minha mãe sorriu e acenou com a cabeça.
                        <br/><br/>
                        "Sim, há algo que você precisa?"
                        <br/><br/>
                        "Ovos. E leite. Estamos sem flocos de milho também."
                        <br/><br/>
                        Ela acenou com a cabeça.
                        <br/><br/>
                        "Bom dia, garota", disse papai. Os dois estavam sentados juntos, tomando café da manhã e então pensei que as coisas haviam voltado ao normal. Ele esvaziou o café e se levantou.
                        <br/><br/>
                        "Hora de trabalhar!" Ele gritou.
                        <br/><br/>
                        "Oh, querido, você poderia conseguir alguns mantimentos no caminho de volta? Sal, peixe e vinagre, por favor."
                        <br/><br/>
                        Mais uma vez, meu pai e eu trocamos um olhar confuso. Bem, o meu era o de confusão, mas seu olhar parecia ser de angústia. Eu balancei a cabeça para silenciosamente dizer a ele que eu tinha as coisas sob controle.
                        <br/><br/>
                        Papai acenou de volta e então começou a se dirigir para a porta.
                        <br/><br/>
                        "Claro, querida."
                        <br/>
                        -
                        <br/>
                        Na quarta-feira eu a encontrei no jardim, cavando terra. Exatamente o oposto do que normalmente estaríamos fazendo naquele dia. Ela estava cavando um buraco, grande e largo, e eu simplesmente não conseguia dizer para que diabos ela estaria fazendo isso. Papai foi embora antes de eu acordar, então não pude falar com ele, mas prometi a mim mesma que faria isso esta noite. Algo estava errado com minha mãe. No começo fiquei preocupada, mas comecei a ficar com medo. Tanto para mim quanto para ela.
                        <br/><br/>
                        "Algo está errado?" Meu pai perguntou naquela noite. Era difícil encontrar um momento sozinha, mamãe sempre parecia estar de alguma forma por perto. Eu odiava ter que me afastar dela para expressar minhas preocupações, mas estava preocupada que ela não estivesse totalmente estável.
                        <br/><br/>
                        "Ela não fez nada que normalmente faz. E você viu aquele buraco lá fora? Pai," parei por um momento ", acho que ela pode realmente precisar de ajuda."
                        <br/><br/>
                        Papai inclinou a cabeça e olhou para o jardim pela janela. Suponho que ele não tenha visto o buraco lá antes.
                        <br/><br/>
                        "Eu não irei trabalhar amanhã. Ficarei aqui com você. Tudo bem?"
                        <br/><br/>
                        Suspirei de alívio. Juntos, poderíamos descobrir o que estava acontecendo e talvez ele pudesse iniciar uma conversa com ela.
                        <br/><br/>
                        "Sim, obrigada, pai. Acho que seria muito útil."
                        <br/>
                        -
                        <br/>
                        Quinta-feira encontrei minha mãe no jardim novamente. Normalmente isso não seria tão estranho, já que eu normalmente a encontraria ali pendurando os lençóis. No entanto, nesta quinta-feira, houve uma tempestade do lado de fora. Tinha chovido toda a manhã e não parecia que iria acabar tão cedo.
                        <br/><br/>
                        "O que você está fazendo?" Eu gritei da porta do jardim. Papai, que me ouviu gritar, apareceu atrás de mim e segurava meu ombro.
                        <br/><br/>
                        Mamãe se virou e riu. E foi então que vimos o que ela realmente estava fazendo lá. Ela estava pendurando no varal as cabeças cortadas do peixe que papai comprou.
                        <br/><br/>
                                <figure class="imagem-texto">
                                    <img src="../fotosCreepys/varalPeixe.jpg" alt="">
                                </figure>
                        <br/>
                        "Mãe?" Eu só consegui emitir um sussurro. "Algo está errado. Precisamos ajudá-la."
                        <br/><br/>
                        Antes que eu pudesse me mover, meu pai já estava correndo para fora e guiando minha mãe de volta para a nossa sala de estar.
                        <br/>
                        -
                        <br/>
                        Quinta-feira se transformou em sexta-feira sem um minuto de sono. Pelo menos para mim. Eu não conseguia fechar meus olhos sem cabeças de peixes aparecendo na minha mente. E a visão de minha mãe naquele mesmo vestido maldito e aquele sorriso estampado.
                        <br/><br/>
                        Cansada e exausta, desci as escadas aos tropeções para encontrar meus pais sentados em frente à televisão. Nem me incomodei em falar com eles e fui direto para a cozinha.
                        <br/><br/>
                        Pela primeira vez em anos, não havia cheiro doce vindo de lá. Não, em vez disso, o fedor de vinagre chegou ao meu nariz. Quase não tínhamos comida em casa. Suponho que mamãe tenha se esquecido completamente disso e papai não queria deixá-la em seu estágio atual. Ele quase não saiu mais do lado dela.
                        <br/><br/>
                        Sem falar com eles comecei com todas as tarefas dos últimos dias, fiz uma lista de compras, limpei e joguei algumas roupas na máquina de lavar. Isso pode soar como se eu só quisesse que minha mãe voltasse a cuidar da casa, mas juro que não tinha nada a ver com isso. Eu simplesmente estava preocupado porque ela não era mais ela mesma.
                        <br/><br/>
                        Na manhã seguinte, todos os itens que escrevi na lista estavam em sacos de papel no balcão da cozinha. Presumo que papai tenha saído para comprá-los.
                        <br/><br/>
                        No sábado, ela fez a coisa mais assustadora até agora. Uma visão que não poderei apagar da minha mente tão cedo. Tudo começou apenas à noite. O dia todo, meus pais agiram normalmente. Mamãe até cozinhou e nós nos sentamos em volta da mesa, conversando e rindo. Ela falou mais e eu poderia jurar que ela era inteiramente ela mesma de novo.
                        <br/><br/>
                        Ou ela aprendeu a se adaptar. Pelo menos em alguns aspectos. Quando a noite chegou, no entanto, tudo ficou totalmente bagunçado.
                        <br/><br/>
                        Acordei com o som de um estilhaçamento alto e imediatamente pulei da cama. Eu estava com os nervos à flor da pele ultimamente e não demorou muito para me assustar. O som vinha do andar de baixo e meu pensamento inicial foi que alguém tinha arrombado a porta, mas então ouvi minha mãe cantarolando algo lá embaixo.
                        <br/><br/>
                        "Mãe?" Eu gritei.
                        <br/><br/>
                        "Vá para a cama, criança!" Ela gritou de volta, mas eu já estava indo para as escadas.
                        <br/><br/>
                        Ela havia pintado as paredes com anagramas estranhos usando tinta vermelha. Ainda usando o mesmo vestido, ela olhou para cima e sorriu, mas desta vez parecia muito menos sincero. Acho que até vi uma contração no olho dela. Papai estava sentado em uma cadeira ao lado dela.
                        <br/><br/>
                        "Mãe. Precisamos de ajuda para você. Vou ligar para o hospital, ok?"
                        <br/><br/>
                        “Não, não. Eu vou ajudar,” papai disse e pegou a lata de tinta da mão dela.
                        <br/><br/>
                        Uma lágrima rolou por sua bochecha, mas o sorriso nunca desapareceu.
                        <br/><br/>
                        "Ora, obrigado, querido."
                        <br/><br/>
                        "As famílias se ajudam", papai sorriu de volta.
                        <br/><br/>
                        "Sim, bem, devo ir comprar mais tinta. A criança vai me ajudar", disse a mãe.
                        <br/><br/>
                        Papai me convocou.
                        <br/><br/>
                        "Que porra é essa? É meio da noite. Olha, mãe, eu realmente sinto muito, mas-,"
                        <br/><br/>
                        Ela manteve minha boca fechada com a mão antes que eu pudesse dizer outra palavra.
                        <br/><br/>
                        "Temos que comprar tinta. Qualquer mãe normal e sua filha fazem exatamente isso. É de extrema importância para um lar decente."
                        <br/><br/>
                        Papai olhou em volta para a tinta vermelha na parede e no rosto de minha mãe e finalmente disse a última coisa que eu esperava.
                        <br/><br/>
                        "Isso parece perfeitamente razoável. Vejo você em breve, querida."
                        <br/><br/>
                        Eu não tinha ideia do que fazer. Parecia que meus pais haviam enlouquecido, mas eu não poderia deixar minha mãe sair sozinha, ou deixá-la dirigir um carro naquele estado. Saí com ela e sentei ao volante com a intenção de levá-la ao hospital. No entanto, assim que comecei a dirigir, não consegui por nada lembrar que direção tomar.
                        <br/><br/>
                        "Continue em frente. Siga a rua", disse minha mãe finalmente. Seu sorriso havia desaparecido e sua perna estava tremendo.
                        <br/><br/>
                        "Apenas continue, querida."
                        <br/><br/>
                        Eu não sabia o que dizer, então continuei em frente até deixarmos o bairro e os arredores também. Nós nos encontrávamos na rodovia quando eu não consegui mais segurar. Encontrei a saída mais próxima e parei.
                        <br/><br/>
                        Minha mãe estava tremendo e na luz escura com respingos de tinta vermelha em seu rosto, finalmente percebi que poderia ter cometido um grande erro.
                        <br/><br/>
                        "Mãe?" Eu mal disse a palavra.
                        <br/><br/>
                        Ela abriu a boca, mas nenhuma palavra saiu a princípio. Ela engoliu em seco e finalmente falou.
                        <br/><br/>
                        "Eu sinto muito, meu amor. Eu não queria acreditar. Não fazia sentido."
                        <br/><br/>
                        "O que você está falando?"
                        <br/><br/>
                        Ela olhou para mim como se eu fosse a única perdendo a cabeça.
                        <br/><br/>
                        "Aquela coisa aí. Aquilo. Aquele não era o seu pai. Você não percebeu? No início, pensei que só agiria ligeiramente diferente para ver se ele notaria que algo estava errado, mas apenas copiava o que quer que estivéssemos fazendo ou dizendo, "ela respirou fundo. "Eu acordei algumas vezes durante a noite. Ele nunca dorme. Ele fica na cama com os olhos abertos esperando que acordemos. Eu ... eu sinto muito. Eu simplesmente não pude acreditar no começo. Eu pensei que talvez algo estava errado com seu pai. Deveríamos ter saído ou pedido ajuda, mas- "ela ficou em silêncio por um momento.
                        <br/><br/>
                        “-Minhas memórias estavam borradas e eu tinha que ter certeza primeiro. Certifiquei-me que era ele e não eu. ”
                        <br/><br/>
                        Minhas mãos não paravam de suar e minha cabeça começou a correr loucamente. Esta semana inteira estive tão focada em minha mãe que não percebi que meu pai apenas concordava com as coisas que eu estava dizendo. Ele não fez nada. E de repente parecia que algo se estilhaçava em minha mente. Minha mãe sempre foi um pouco peculiar. Especialmente depois que meu pai faleceu.
                        <br/><br/>
                        "Mãe. Essa não era a nossa casa, era?" Eu engoli em seco. "Como acabamos aí?"
                        <br/><br/>
                        Ela olhou para mim, um reflexo do meu próprio medo e confusão.
                        <br/><br/>
                        "Eu não tenho absolutamente nenhuma ideia. Deve ter de alguma forma nos atraído para lá e nos fez esquecer. "
        

