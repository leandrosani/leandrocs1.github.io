                        <br/><br/>
                        Por ser uma casa bem pequena, não é muito à prova de som, por isso é fácil saber onde alguém está. Geralmente se entra pela porta lateral, e esta se abre para um longo corredor. Porta do porão à esquerda, porta do quarto à direita, cozinha à esquerda, sala de estar e sala de jantar à direita, banheiro na outra extremidade da casa. A sala de jantar estava separada da sala de estar por uma parede, e tinha sido a fonte da maioria das coisas de que ela me relatou.
